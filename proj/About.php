<h2><strong>LED Lamp</strong></h2>
</br>
<p><img src="image/bulb1.jpg" style="float:right;width:204px;height:128px;">An <b>LED lamp</b> is a light-emitting diode (LED) product that is assembled into a lamp (or light bulb) for use in lighting fixtures. LED lamps have a lifespan and electrical efficiency which are several times greater than incandescent lamps, and are significantly more efficient than most fluorescent lamps,[1][2][3] with some chips able to emit more than 300 lumens per watt (as claimed by Cree and some other LED manufacturers).[4] The LED lamp market is projected to grow by more than twelve-fold over the next decade, from $2 billion in the beginning of 2014 to $25 billion in 2023, a compound annual growth rate (CAGR) of 25%.[5] As of 2016, LEDs use only about 10% of the energy an incandescent lamp requires.[6]</p>
<p><img src="image/bulb2.jpg" style="float:right;width:204px;height:128px;">Like incandescent lamps and unlike most fluorescent lamps (e.g. tubes and compact fluorescent lamps or CFLs), LEDs come to full brightness without need for a warm-up time; the life of fluorescent lighting is also reduced by frequent switching on and off.[7] The initial cost of LED is usually higher. Degradation of LED dye and packaging materials reduces light output to some extent over time.</p>
